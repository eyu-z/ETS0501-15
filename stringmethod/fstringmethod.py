# example 1

my_string = "Hello, Python!"

string_length = len(my_string)

print(f"The length of the string '{my_string}' is: {string_length}")

# Output: The length of the string 'Hello, Python!' is: 13

# example 2

my_list = [10, 20, 30, 40, 50]

list_length = len(my_list)

print(f"The length of the list '{my_list}' is: {list_length}")

# Output: The length of the list '[10, 20, 30, 40, 50]' is: 5

# example 3

my_tuple = ('apple', 'banana', 'cherry')

tuple_length = len(my_tuple)

print(f"The length of the tuple '{my_tuple}' is: {tuple_length}")

# Output: The length of the tuple '('apple', 'banana', 'cherry')' is: 3

# example 4

my_set = {1, 2, 3, 3, 4}  # Note the duplicate '3', sets only store unique elements

set_length = len(my_set)

print(f"The number of elements in the set '{my_set}' is: {set_length}")

# Output: The number of elements in the set '{1, 2, 3, 4}' is: 4