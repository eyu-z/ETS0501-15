# Example 1: Reversing a list of numbers

numbers = [1, 2, 3, 4, 5]

numbers.reverse()

print(numbers)                # Output: [5, 4, 3, 2, 1]

# Example 2: Reversing a list of strings

fruits = ["apple", "banana", "orange"]

fruits.reverse()

print(fruits)                 # Output: ['orange', 'banana', 'apple']

# Example 3: Reversing a list with mixed data types

mixed_list = [1, "hello", 3.14, True]

mixed_list.reverse()

print(mixed_list)              # Output: [True, 3.14, 'hello', 1]